
package evennumber;


public class Evennumber {

    
    public static void main(String[] args) {
        int n=100;
        System.out.println("list of even numbers from 10 to 100 are:");
        for(int i=10;i<=n;i++)
        {
            if(i%2==0)
            {
                System.out.println(i+ " ");
            }
        }
        
    }
    
}
